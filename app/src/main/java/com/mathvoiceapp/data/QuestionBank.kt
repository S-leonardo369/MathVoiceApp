    }

    fun getAllForMode(topics: List<Topic>, difficulties: List<Difficulty>): List<MathQuestion> {
        return allQuestions
            .filter { it.topic in topics && it.difficulty in difficulties }
            .shuffled()
    }

    fun getAvailableTopics(): List<Topic> = Topic.values().toList()

    fun getQuestionCount(topic: Topic, difficulty: Difficulty): Int =
        allQuestions.count { it.topic == topic && it.difficulty == difficulty }
}
